	  download: function(url, filename, onstart, onprogress, onload, onerror) {
	    onstart();
	    this.currentRequest = GM_xmlhttpRequest({
	      method: "GET",
	      url,
	      responseType: "blob",
	      onprogress: function(event) {
	        if (event.lengthComputable) {
	          const percent = (event.loaded / event.total * 100).toFixed(2);
	          onprogress(percent, event.loaded, event.total);
	        }
	      },
	      onload: function(response) {
	        if (response.status < 200 || response.status >= 300 || !response.response) {
	          onerror(new Error(`Request failed with status ${response.status}`));
	          return;
	        }
	        const blob = response.response;
	        const a = document.createElement("a");
	        const objectUrl = URL.createObjectURL(blob);
	        a.href = objectUrl;
	        a.download = filename;
	        document.body.appendChild(a);
	        a.click();
	        a.remove();
	        setTimeout(() => URL.revokeObjectURL(objectUrl), 1e3);
	        onload();
	      },
	      onerror: function(err) {
	        onerror(err);
	      },
	      onabort: function() {
	        onerror(new Error("Request aborted"));
	      },
	      ontimeout: function() {
	        onerror(new Error("Request timeout"));
	      }
	    });
	  },
	  getFileExtension: function(url) {
	    try {
	      const videoSource = document.querySelector("video source");
	      const imageSource = document.querySelector("img");
	      let type = "";
	      if (typeof videoSource === "string" && videoSource) {
	        url = videoSource;
	      } else if (videoSource instanceof HTMLSourceElement && (videoSource.src || videoSource.type)) {
	        url = videoSource.src || "";
	        type = videoSource.type || "";
	      }
	      if (!(videoSource instanceof HTMLSourceElement) && !videoSource && imageSource) {
	        url = String(url || "");
	      }
	      const typeMap = {
	        "audio/mpeg": "mp3",
	        "audio/mp4": "mp4",
	        "audio/ogg": "ogg",
	        "audio/wav": "wav",
	        "audio/webm": "webm",
	        "audio/aac": "aac",
	        "audio/flac": "flac",
	        "audio/x-wav": "wav",
	        "audio/x-ms-wma": "wma",
	        "video/mp4": "mp4",
	        "video/webm": "webm",
	        "video/ogg": "ogv",
	        "video/quicktime": "mov",
	        "video/x-msvideo": "avi",
	        "video/x-ms-wmv": "wmv",
	        "video/mpeg": "mpeg",
	        "video/3gpp": "3gp",
	        "image/jpeg": "jpg",
	        "image/jpg": "jpg",
	        "image/png": "png",
	        "image/gif": "gif",
	        "image/webp": "webp",
	        "image/svg+xml": "svg",
	        "image/bmp": "bmp",
	        "image/tiff": "tiff",
	        "image/x-icon": "ico"
	      };
	      const normalizedType = String(type).split(";")[0].trim().toLowerCase();
	      if (normalizedType && typeMap[normalizedType]) {
	        return typeMap[normalizedType];
	      }
	      if (!url) {
	        return null;
	      }
	      const cleanUrl = String(url).split("?")[0].split("#")[0];
	      const fileName = cleanUrl.split("/").pop();
	      if (!fileName) {
	        return null;
	      }
	      const match = fileName.match(/\.([a-zA-Z0-9]+)$/);
	      return match ? match[1].toLowerCase() : null;
	    } catch (error) {
	    }
	    return null;
	  },
	  generateFilename: function(url) {
	    let ext = "";
	    try {
	      const parsedUrl = new URL(String(url));
	      ext = parsedUrl.searchParams.get("____ext_") || "";
	    } catch (err) {
	      const match = String(url).match(/[?&]____ext_=([^&#]*)/);
	      ext = match ? decodeURIComponent(match[1]) : "";
	    }
	    ext = String(ext).trim().toLowerCase().replace(/^\./, "");
	    if (!ext || ext === "unknown") {
	      ext = this.getFileExtension(url);
	    }
	    ext = String(ext || "").trim().toLowerCase().replace(/^\./, "").replace(/[^a-z0-9]/g, "");
	    if (!ext || ext === "unknown") {
	      ext = "mp4";
	    }
	    return `${Date.now()}.${ext}`;
	  },
